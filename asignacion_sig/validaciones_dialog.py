    def aplicarPermisosUsuario(self, host, bd, usuario_admin, contrasena, usuario_objetivo, permisos_activos, tablas):
        """Apply selected permissions to a user on all specified tables.
        
        Performs the actual GRANT and REVOKE operations:
        1. Revokes ALL permissions on all tables first
        2. Then grants only the selected permissions
        3. Commits all changes to the database
        4. Logs all operations for audit trail
        
        This two-step approach ensures the user has exactly the permissions
        specified, with no extra permissions remaining.
        
        Args:
            host (str): PostgreSQL server hostname or IP
            bd (str): Database name
            usuario_admin (str): Admin username for authentication
            contrasena (str): Admin password for authentication
            usuario_objetivo (str): Username to apply permissions to
            permisos_activos (list): List of permissions to grant (SELECT, INSERT, UPDATE, DELETE)
            tablas (list): List of tuples (schema, table, ...) to apply permissions to
        
        Returns:
            None
        
        Raises:
            Exception: If database operations fail
        
        Side Effects:
            - Executes REVOKE ALL statements on all tables
            - Executes GRANT statements for selected permissions
            - Commits changes to the database
            - Logs all operations to audit trail
        
        Security Notes:
            - All identifiers are quoted to prevent SQL injection
            - Uses parameterized connections (psycopg2)
            - Only performs GRANT/REVOKE operations; no data is modified
        
        Example:
            >>> dialog.aplicarPermisosUsuario(
            ...     'localhost', 'mydb', 'admin', 'password',
            ...     'user1', ['SELECT', 'INSERT'],
            ...     [('public', 'table1'), ('public', 'table2')]
            ... )
        """
        import psycopg2
        
        try:
            with psycopg2.connect(
                host=host, 
                database=bd, 
                user=usuario_admin, 
                password=contrasena, 
                port='5432'
            ) as conn:
                with conn.cursor() as cursor:
                    # Registrar inicio de operación
                    self._registrar_auditoria(
                        "ACTUALIZACIÓN_INICIO",
                        f"Usuario: {usuario_objetivo}, Permisos: {', '.join(permisos_activos)}",
                        usuario_admin
                    )
                    
                    permisos_quitados = []
                    permisos_nuevos = []
                    
                    # Primero obtener permisos actuales
                    from .permisos import obtenerPermisosDetallados
                    permisos_actuales = obtenerPermisosDetallados(host, bd, usuario_admin, contrasena, usuario_objetivo)
                    permisos_actuales_set = set()
                    if permisos_actuales['SELECT']:
                        permisos_actuales_set.add('SELECT')
                    if permisos_actuales['INSERT']:
                        permisos_actuales_set.add('INSERT')
                    if permisos_actuales['UPDATE']:
                        permisos_actuales_set.add('UPDATE')
                    if permisos_actuales['DELETE']:
                        permisos_actuales_set.add('DELETE')
                    
                    # Calcular permisos nuevos y quitados
                    permisos_activos_set = set(permisos_activos)
                    permisos_nuevos = list(permisos_activos_set - permisos_actuales_set)
                    permisos_quitados = list(permisos_actuales_set - permisos_activos_set)
                    
                    # Primero revocar todos los permisos
                    for esquema, tabla, _, _, _, _ in tablas:
                        try:
                            cursor.execute(f'REVOKE ALL ON TABLE "{esquema}"."{tabla}" FROM "{usuario_objetivo}";')
                            self._registrar_auditoria(
                                "REVOKE_ALL",
                                f"Usuario: {usuario_objetivo}, Tabla: {esquema}.{tabla}",
                                usuario_admin
                            )
                        except psycopg2.Error:
                            pass
                    
                    # Luego otorgar solo los permisos seleccionados
                    for permiso in permisos_activos:
                        for esquema, tabla, _, _, _, _ in tablas:
                            try:
                                cursor.execute(f'GRANT {permiso} ON TABLE "{esquema}"."{tabla}" TO "{usuario_objetivo}";')
                                self._registrar_auditoria(
                                    "GRANT",
                                    f"Usuario: {usuario_objetivo}, Permiso: {permiso}, Tabla: {esquema}.{tabla}",
                                    usuario_admin
                                )
                            except psycopg2.Error:
                                pass
                    
                    conn.commit()
                    
                    # Registrar finalización
                    self._registrar_auditoria(
                        "ACTUALIZACIÓN_COMPLETADA",
                        f"Usuario: {usuario_objetivo}, Permisos: {', '.join(permisos_activos)}",
                        usuario_admin
                    )
                    
                    # Registrar en la base de datos 00_LogGestionUsuarios
                    try:
                        permisos_log = ""
                        if permisos_nuevos:
                            permisos_log += f"Nuevo({','.join(permisos_nuevos)})"
                        if permisos_quitados:
                            if permisos_log:
                                permisos_log += " - "
                            permisos_log += f"Quitados({','.join(permisos_quitados)})"
                        
                        if permisos_log:
                            registrarLogGestion(
                                host=host,
                                usuario_admin=usuario_admin,
                                contrasena=contrasena,
                                tipo_gestion='ACTUALIZACION PERMISOS',
                                bd_objetivo=bd,
                                usuario_gestionado=usuario_objetivo,
                                permisos=permisos_log,
                                usuario_gestion=usuario_admin
                            )
                    except Exception as e:
                        self._registrar_auditoria(
                            "LOG_ERROR",
                            f"Error al registrar log: {str(e)}",
                            usuario_admin
                        )
            
        except Exception as e:
            self._registrar_auditoria(
                "ACTUALIZACIÓN_ERROR",
                f"Usuario: {usuario_objetivo}, Error: {str(e)}",
                usuario_admin
            )
            raise Exception(f"Error al aplicar permisos: {str(e)}")
    
