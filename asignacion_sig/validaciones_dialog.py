    def _validar_usuario_existe(self, cursor, usuario):
        """Valida si un usuario existe en PostgreSQL.
        
        Args:
            cursor: Cursor de psycopg2
            usuario (str): Nombre del usuario a validar
        
        Returns:
            bool: True si el usuario existe, False en caso contrario
        """
        try:
            query = "SELECT 1 FROM pg_user WHERE usename = %s"
            cursor.execute(query, (usuario,))
            return cursor.fetchone() is not None
        except:
            return False
    
