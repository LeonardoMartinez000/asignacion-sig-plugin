    def revocarTodosPermisosUsuario(self, host, bd, usuario_admin, contrasena, usuario_objetivo, tablas):
        """Revoke all permissions from a user on all tables and schemas.
        
        Performs the actual REVOKE operations:
        1. Revokes ALL permissions on all specified tables
        2. Revokes CONNECT permission on the database
        3. Commits all changes to the database
        4. Logs all operations for audit trail
        
        This method completely removes all permissions from the user, leaving them
        with no access to any tables or the database itself.
        
        Args:
            host (str): PostgreSQL server hostname or IP
            bd (str): Database name
            usuario_admin (str): Admin username for authentication
            contrasena (str): Admin password for authentication
            usuario_objetivo (str): Username to revoke permissions from
            tablas (list): List of tuples (schema, table, ...) to revoke permissions from
        
        Returns:
            None
        
        Raises:
            Exception: If database operations fail
        
        Side Effects:
            - Executes REVOKE ALL statements on all tables
            - Executes REVOKE CONNECT statement on the database
            - Commits changes to the database
            - Logs all operations to audit trail
        
        Security Notes:
            - All identifiers are quoted to prevent SQL injection
            - Uses parameterized connections (psycopg2)
            - Only performs REVOKE operations; no data is modified
            - Errors in individual REVOKE statements are silently ignored
              to ensure all revocations are attempted
        
        Example:
            >>> dialog.revocarTodosPermisosUsuario(
            ...     'localhost', 'mydb', 'admin', 'password',
            ...     'user1', [('public', 'table1'), ('public', 'table2')]
            ... )
        """
        import psycopg2
        
        try:
            with psycopg2.connect(
                host=host, 
                database=bd, 
                user=usuario_admin, 
                password=contrasena, 
                port='5432'
            ) as conn:
                with conn.cursor() as cursor:
                    # Registrar inicio de operación
                    self._registrar_auditoria(
                        "REVOCACIÓN_TOTAL_INICIO",
                        f"Usuario: {usuario_objetivo}",
                        usuario_admin
                    )
                    
                    permisos_quitados = []
                    
                    # Obtener permisos actuales antes de revocar
                    from .permisos import obtenerPermisosDetallados
                    permisos_actuales = obtenerPermisosDetallados(host, bd, usuario_admin, contrasena, usuario_objetivo)
                    if permisos_actuales['SELECT']:
                        permisos_quitados.append('SELECT')
                    if permisos_actuales['INSERT']:
                        permisos_quitados.append('INSERT')
                    if permisos_actuales['UPDATE']:
                        permisos_quitados.append('UPDATE')
                    if permisos_actuales['DELETE']:
                        permisos_quitados.append('DELETE')
                    
                    # Revocar permisos en todas las tablas
                    for esquema, tabla, _, _, _, _ in tablas:
                        try:
                            cursor.execute(f'REVOKE ALL ON TABLE "{esquema}"."{tabla}" FROM "{usuario_objetivo}";')
                            self._registrar_auditoria(
                                "REVOKE_ALL",
                                f"Usuario: {usuario_objetivo}, Tabla: {esquema}.{tabla}",
                                usuario_admin
                            )
                        except psycopg2.Error:
                            pass
                    
                    # Revocar conexión a la BD
                    try:
                        cursor.execute(f'REVOKE CONNECT ON DATABASE "{bd}" FROM "{usuario_objetivo}";')
                        self._registrar_auditoria(
                            "REVOKE_CONNECT",
                            f"Usuario: {usuario_objetivo}, Base de datos: {bd}",
                            usuario_admin
                        )
                    except psycopg2.Error:
                        pass
                    
                    conn.commit()
                    
                    # Registrar finalización
                    self._registrar_auditoria(
                        "REVOCACIÓN_TOTAL_COMPLETADA",
                        f"Usuario: {usuario_objetivo}",
                        usuario_admin
                    )
                    
                    # Registrar en la base de datos 00_LogGestionUsuarios
                    try:
                        permisos_str = ','.join(permisos_quitados) if permisos_quitados else 'TODOS'
                        registrarLogGestion(
                            host=host,
                            usuario_admin=usuario_admin,
                            contrasena=contrasena,
                            tipo_gestion='QUITAR PERMISOS',
                            bd_objetivo=bd,
                            usuario_gestionado=usuario_objetivo,
                            permisos=permisos_str,
                            usuario_gestion=usuario_admin
                        )
                    except Exception as e:
                        self._registrar_auditoria(
                            "LOG_ERROR",
                            f"Error al registrar log: {str(e)}",
                            usuario_admin
                        )
            
        except Exception as e:
            self._registrar_auditoria(
                "REVOCACIÓN_ERROR",
                f"Usuario: {usuario_objetivo}, Error: {str(e)}",
                usuario_admin
            )
            raise Exception(f"Error al revocar permisos: {str(e)}")
